CREATE SCHEMA IF NOT EXISTS news_schema;

CREATE TABLE IF NOT EXISTS news_schema.users (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    password VARCHAR(255) NOT NULL,
    email VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS news_schema.roles (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    role VARCHAR(255) NOT NULL,
    username INT NOT NULL
);

CREATE TABLE IF NOT EXISTS news_schema.news_category (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category VARCHAR(255) NOT NULL
);

CREATE TABLE IF NOT EXISTS news_schema.news (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    text VARCHAR(255) NOT NULL,
    user_id INT NOT NULL,
    category_id INT NOT NULL
);

CREATE TABLE IF NOT EXISTS news_schema.reviews (
    id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    text VARCHAR(255) NOT NULL,
    user_id INT NOT NULL,
    news_id INT NOT NULL
);
